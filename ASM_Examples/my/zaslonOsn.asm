ZASOSN	START 0
	LDA CRKAB
	STCH @ZASLON
	
	LDA ZASLON
	ADD WIDTH
	SUB #1
	STA TEMP
	LDA CRKAB
	STCH @TEMP
	
	LDA HEIGHT
	MUL WIDTH
	RMO A,T
	SUB #1
	ADD ZASLON
	STA TEMP
	LDA CRKAB
	STCH @TEMP
	
	RMO T,A
	SUB WIDTH
	ADD ZASLON
	STA TEMP
	LDA CRKAB
	STCH @TEMP
	
	
	
HALT	J HALT	
	
	
CRKAB	BYTE X'000042'
TEMP	RESW 1
. privzeti podatki o "zaslonu"
ZASLON  WORD X'00B800'
WIDTH   BYTE X'000050'
HEIGHT  BYTE X'000019'
        END ZASOSN
        
