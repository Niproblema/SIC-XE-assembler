FIBB	START 0
	JSUB STACKINIT
	LDA STEVILO
	JSUB FIBBR
	STA REZUL
	
HALT	J HALT

STEVILO	WORD 20
REZUL	RESW 1

FIBBR	COMP #2
	JLT IZHOD
	
	STL @STACKPTR
	JSUB STACKPUSH
	STT @STACKPTR
	JSUB STACKPUSH
	STS @STACKPTR
	JSUB STACKPUSH
	
	SUB #1
	RMO A,T
	SUB #1
	RMO A,S
	JSUB FIBBR
	RMO A,S
	RMO T,A
	JSUB FIBBR
	ADDR S,A	
	
	JSUB STACKPOP
	LDS @STACKPTR	
	JSUB STACKPOP
	LDT @STACKPTR
	JSUB STACKPOP
	LDL @STACKPTR
IZHOD	RSUB




STACKINIT	STA TEMP
	LDA #STACK
	STA STACKPTR
	LDA TEMP
	RSUB

STACKPUSH	STA TEMP
	LDA STACKPTR
	ADD #3
	STA STACKPTR
	LDA TEMP
	RSUB	

STACKPOP	STA TEMP
	LDA STACKPTR
	SUB #3
	STA STACKPTR
	LDA TEMP
	RSUB

.SKLAD
STACK	RESW 1000
STACKPTR	RESW 1
TEMP	RESW 1

	END FIBB
